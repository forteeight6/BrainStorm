# Tipos de dados
# Para analisar os dados, é importante saber com que tipo de dados estamos lidando.

# Podemos dividir os tipos de dados em três categorias principais:

# Numérico
# Categórico
# Ordinal
# Os dados numéricos são números e podem ser divididos em duas categorias numéricas:

# Dados discretos

# - números limitados a inteiros. Exemplo: o número de carros que passam.

# Dados contínuos

# - números de valor infinito. Exemplo: o preço de um item ou o tamanho de um item
# Os dados categóricos são valores que não podem ser comparados uns com os outros. Exemplo: um valor de cor ou quaisquer valores sim / não.

# Os dados ordinais são como dados categóricos, mas podem ser comparados uns com os outros. Exemplo: notas escolares em que A é melhor que B e assim por diante.

# Conhecendo o tipo de dados de sua fonte de dados, você saberá qual técnica usar para analisá-los.

