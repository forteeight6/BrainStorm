# Modificando dicionários em Python
# Ao contrário de tuplas, dicionários podem ser modificados:

# Modificando um dicionário.
paises = dict()

paises["BRA"] = "Brazil"
paises["FRA"] = "France"

# Adicionando um elemento.
paises["ESP"] = "Espanha"

print("Dicionário modificado: ", paises)
